    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        String jenis = null;
        if(rlaki.isSelected()){
            jenis = "Laki-Laki";
        }
        else if(rperempuan.isSelected()){
            jenis = "Perempuan";
        }
         try{
            String sql = "Update tbl_pelanggan set Nama=?,Jenis_Kelamin=?,Alamat=?,No_Telpon=? where No_KTP='"+txt_ktp.getText()+"'";
            PreparedStatement stt = con.prepareStatement(sql);
            stt.setString(1, txt_nama.getText());
            stt.setString(2, jenis);
            stt.setString(3, txt_alamat.getText());
            stt.setString(4, txt_tlp.getText());
            
            stt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            kosong();
            txt_ktp.requestFocus();
            }catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data Gagal Diubah"+ e);
            }
            datatable();
        
    }//GEN-LAST:event_btn_editActionPerformed

