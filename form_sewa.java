    private void autonumber(){
        String Id_Sewa="";
        try{
            String sql= "SELECT Id_Sewa FROM tbl_sewa order by id_Sewa asc";
            PreparedStatement stt= con.prepareStatement(sql);
            ResultSet res= stt.executeQuery(sql);
            
            while(res.next()){
                Id_Sewa= res.getString("Id_Sewa");
            }
            }catch(SQLException sqle){Id_Sewa="";}
        
        if(Id_Sewa.length()<1){Id_Sewa="Is0000";}
        String ur= Id_Sewa.substring(2);
        int u= Integer.parseInt(ur)+1;
        System.out.println(ur+"=="+u);
            if(u<10)
            {ur="000"+u;}
            else if(u<100)
            {ur="00"+u;}
            else if(u<1000)
            {ur="0"+u;}
            else
            {ur=""+u;}
        Id_Sewa= "Is"+ur;
        txt_id.setText(Id_Sewa);
    }
    
