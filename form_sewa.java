    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        String sql = "insert into tbl_sewa values (?,?,?,?,?,?,?,?,?,?)";
        try{
           PreparedStatement stt = con.prepareStatement(sql);
           stt.setString(1, txt_id.getText());
           stt.setString(2, txt_nama.getText());
           stt.setString(3, txt_tlp.getText());
           stt.setString(4, txt_no.getText());
           stt.setString(5, txt_tipe.getText());
           stt.setString(6, txt_tarif.getText());
           stt.setString(7, datachooser);
           stt.setString(8, datachooser1);
           stt.setString(9, txt_lama.getText());
           stt.setString(10, txt_total.getText());
           
           stt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            txt_id.requestFocus();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+ e);
        }
        datatable();
        autonumber();
        
    }//GEN-LAST:event_tambahActionPerformed

